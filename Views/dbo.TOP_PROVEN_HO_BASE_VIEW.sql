LEFT JOIN dbo.WELLNESS WT ON WT.UID = M.UID
WHERE XF.ACTIVE<>0 AND  (XF.YS Is Null Or XF.YS='')  AND XF.BREED = 'HO'

UNION ALL SELECT M.UID, '' AS IND, ABK.NEW, ABK.REGNAME, ABK.RECESS, H.HCD, ABK.CODENO AS CODENO,0 AS SEXED_ONLY, M.NDTRS, M.NHRDS, M.PTAM, M.PTAF_P, M.PTAF,
 M.REL_MF, M.PTAP_P, M.PTAP,M.REL_YLD, M.PTAT, M.TREL, M.UDC, M.FLC, M.TPI, M.NM, M.PL, M.REL_PL, M.FE, M.SCS, M.REL_SCS, M.FI, M.DPR, 
M.REL_DPR, M.SCE, M.SCE_REL, WT.WT, WT.DWP, M.SCR, M.REL_SCR, M.SERV_SCR, 
CASE WHEN M.MKTG_PEDIGREE IS NOT NULL THEN M.MKTG_PEDIGREE WHEN M.PEDIGREE IS NOT NULL THEN M.PEDIGREE ELSE ABK.PEDIGREE END AS MKTG_PEDIGREE, ABK.ACTIVE, 'C' AS STYPE, 
235 AS STUD, M.LIV, M.REL_LIV, M.SGL, M.SGL_REL, M.PKEY, M.TKEY, ABK.CODENAME
FROM (dbo.Megadairy AS M INNER JOIN dbo.ABKBULLS AS ABK ON M.UID = ABK.UID)
LEFT JOIN dbo.WELLNESS WT ON WT.UID = M.UID
LEFT JOIN dbo.HaplotypeData H on M.UID = H.UID
