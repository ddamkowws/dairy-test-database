LEFT JOIN dbo.WELLNESS WT ON WT.UID = M.UID
LEFT JOIN dbo.HaplotypeData H on M.UID = H.UID
WHERE (((WWS.SEXED_ONLY)=0) AND ((BI.ACTIVE)<>0) AND ((BI.stype)='C') AND ((WWS.YS) Is Null Or (WWS.YS)='') AND ((WWS.BREED)='HO')) OR (((WWS.SEXED_ONLY)<>0) AND ((BI.ACTIVE)<>0) 
AND ((BI.stype)='X') AND ((WWS.YS) Is Null Or (WWS.YS)='') AND ((WWS.BREED)='HO')) 

UNION ALL SELECT M.UID, '' AS IND, XF.NEW, XF.REGNAME, XF.RECESS,H.HCD, XF.CODENO AS CODENO,0 AS SEXED_ONLY, M.NDTRS, M.NHRDS, M.PTAM, M.PTAF_P, 
