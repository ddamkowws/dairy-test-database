AS
SELECT HQ.CODENO, HCP.REGNAME, HQ.PEDIGREE, HCP.NDTRS AS NDTRS_p, HQ.NDTRS AS NDTRS_c, HQ.NHRDS AS NHRDS_c, HCP.PTAM AS PTAM_p, HQ.PTAM AS PTAM_c, HCP.PTAF_P AS PTAF_P_p, HQ.PTAF_P AS PTAF_P_c, 
