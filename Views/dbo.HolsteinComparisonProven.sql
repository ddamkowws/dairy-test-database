                  HQ.BWC AS BWC_c, HCP.DC AS DC_p, HQ.DC AS DC_c, HCP.CFI AS CFI_p, HQ.CFI AS CFI_c, HCP.ACE AS ACE_p, HQ.ACE AS ACE_c, HCP.KC AS KC_p, HQ.KC AS KC_c, HCP.IBR AS IBR_p, HQ.IBR AS IBR_c, HCP.LEU AS LEU_p, 
                  HQ.LEU AS LEU_c, HCP.BT AS BT_p, HQ.BT AS BT_c, HCP.EC AS EC_p, HQ.EC AS EC_c, HCP.PL AS PL_p, HQ.PL AS PL_c, HCP.NM AS NM_p, HQ.NM AS NM_c, HCP.SCS AS SCS_p, HQ.SCS AS SCS_c, HCP.DPR AS DPR_p, 
                  HQ.DPR AS DPR_c, HCP.SOURCE AS SOURCE_p, HQ.SOURCE AS SOURCE_c, HCP.EVALDATE AS EVALDATE_p, HQ.EVALDATE AS EVALDATE_c, HQ.STYPE
FROM     dbo.HolsteinComparePrevious AS HCP LEFT OUTER JOIN
                  dbo.HPRICE_HISTORY ON HCP.CODENO = dbo.HPRICE_HISTORY.CODENO LEFT OUTER JOIN
                  dbo.HOLSTEINQUERY AS HQ ON HCP.IDNO = HQ.IDNO AND HCP.COUNTRY = HQ.COUNTRY AND HCP.BREED = HQ.BREED
GO

